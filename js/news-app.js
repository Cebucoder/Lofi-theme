let newsMod = document.getElementById("show-news-modal");
let newscardcont = document.getElementById("news-card-con");

function newsModal(){
    newsMod.classList.add("show-news-modal");
    newscardcont.classList.add("show-news-card-cont");
}

function closeNewsModal(){
    newsMod.classList.remove("show-news-modal");
    newscardcont.classList.remove("show-news-card-cont");
}



